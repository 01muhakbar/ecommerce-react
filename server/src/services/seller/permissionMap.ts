export function getPermissionKeysForSellerRole(roleCode: string | null | undefined): string[] {
  if (!roleCode) return [];
  return [...new Set(SELLER_ROLE_PERMISSION_MAP[roleCode] || [])];
}

export function hasSellerPermission(permissionKeys: string[], requiredPermission: string) {
  return Array.isArray(permissionKeys) && permissionKeys.includes(requiredPermission);
}
